int PlayerTurn ;
char PlayerSeed ;
int PlayerRole;

void StuffInit (void)
{
	TExaS_Init(SSI0_Real_Nokia5110_Scope);  // set system clock to 80 MHz
  Nokia5110_Init();
}
void WelcomeScene (void)
{
	Nokia5110_ClearBuffer();
	Nokia5110_DisplayBuffer();
	Nokia5110_PrintBMP(17, 38,Logo, 0);
	Nokia5110_DisplayBuffer();
}
int SelectModeScene (void)
{
	Nokia5110_Clear();
	Nokia5110_SetCursor (2,1);
	Nokia5110_OutString ("1.Single.");
	Nokia5110_SetCursor (2,3);
	Nokia5110_OutString ("2.Multi.");
	return PlayerTurn;
}

char SelectSeedScene(void)
{
	Nokia5110_Clear();
	Nokia5110_SetCursor (0,1);
	Nokia5110_OutString ("Press:");
	Nokia5110_SetCursor (0,2);
	Nokia5110_OutString ("X.player 1 .");
	Nokia5110_SetCursor (0,3);
	Nokia5110_OutString ("O.player 2 .");
	return PlayerSeed ;
}
void Delay100ms(unsigned long count){unsigned long volatile time;
  while(count>0){
    time = 727240;  // 0.1sec at 80 MHz
    while(time){
	  	time--;
    }
    count--;
  }
}

void WaitingScene(void)
{
	int i;
	
	Nokia5110_Clear();
	Nokia5110_SetCursor(1,2);
	Nokia5110_OutString ("Waiting");
	for (i=0;i<3;i=i+1)
	{
		Delay100ms(1);
		Nokia5110_OutString (".");
	}
}
