void stateScene(enum GameState gameState, char PlayerSeed)
{
	char* statement;

	Nokia5110_Clear();
	if ((gameState==P1WIN&&PlayerSeed=='X') || (gameState==P2WIN&&PlayerSeed=='O'))
	{
		Nokia5110_SetCursor (2,2);
		statement = "YOU WIN";
	}
	else if  ((gameState==P1WIN&&PlayerSeed=='O') || (gameState==P2WIN&&PlayerSeed=='X'))
	{
		Nokia5110_SetCursor (1,2);
		statement = "YOU  LOSE";
	}
	else if (gameState==TIE)
	{
		Nokia5110_SetCursor (4,2);
		statement = "TIE";
	}
	
	Nokia5110_OutString (statement);
}

void drawSeed(char seed, int col ,int row)
{
	const unsigned char *Seed ;
	if (seed=='X'||seed=='x') Seed=X;
	else if (seed=='O'||seed=='o') Seed=O;
	if (row > 5 || col < 0 ||col >6) //invalid
		return ;
	
	Nokia5110_PrintBMP(PaddingX+BoxLineWidth*(col+1)+BoxWidth*col,(BoardHeight-PaddingY)-BoxLineWidth*(row+1)-BoxWidth*row,Seed,0);
	Nokia5110_DisplayBuffer();

}

